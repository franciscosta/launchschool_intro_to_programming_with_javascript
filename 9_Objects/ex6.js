/*

  Which of the following values are primitive
  values? Which are objects? Which are neither?

*/

"foo"
// Primitive: Number

3.1415
// Primitive: Number

[ 'a', 'b', 'c' ]
// Object: Array

false
// Primitive: Boolean

foo
// Neither

function bar() { return "bar"; }
// Object: Function

undefined
// Primitive: undefined

let obj = { a: 1, b: 2 };
// Object: Key-Value Object

